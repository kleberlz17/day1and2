# Arrays - Pra grandes quantidades de itens na lista
from array import array

# u = unicode character
# i = integer
# f = float
letras = array("u", ["a", "b", "c", "d"])
numeros_i = array("i", [10, 20, 30, 40])
numeros_f = array("f", [1.2, 2.2, 3.2])

print(letras)
print(numeros_i)
print(numeros_f)

